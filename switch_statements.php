<?php

$color = "black";

switch ($color) {
    case "red":
        echo "The color is red.";
        break;

    case "blue":
        echo "The color is blue.";
        break;

    case "black":
        echo "The color is black.";
        break;

    case "yellow":
        echo "The color is yellow.";
        break;
        
    default:
        echo "The color is not known.";
}